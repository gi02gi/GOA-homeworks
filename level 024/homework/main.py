# ინდექსი — ელემენტის ნომერი სიაში, რომელიც იწყება 0-იდან.
# მაგ: 
fruits = ["ვაშლი", "ბანანი", "ფორთოხალი", "ყურძენი"]
print(fruits[0])   # 0 ინდექსი - "ვაშლი"
print(fruits[2])   # 2 ინდექსი - "ფორთოხალი"
print(fruits[-1])  # -1 ინდექსი - ბოლო ელემენტი "ყურძენი".