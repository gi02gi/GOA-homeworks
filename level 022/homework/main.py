# პირველი დავალება
number = 5          # საიდუმლო რიცხვი, რომელსაც მომხმარებელმა უნდა გამოიცნოს
attempt = 3         # რამდენი ცდა აქვს მოთამაშეს
user_input = ""     # მომხმარებლის შეყვანილი მნიშვნელობა (დასაწყისში ცარიელია)

# ციკლი გრძელდება მანამდე, სანამ მომხმარებლის მიერ შეყვანილი რიცხვი არ უდრის საიდუმლო რიცხვს
# და სანამ ცდების რაოდენობა 0-ზე მეტია
while user_input != number and attempt > 0:
    user_input = int(input("Guess the number between 1-10: "))  # მომხმარებელს ეკითხება რიცხვი (1-10)
    attempt -= 1  # თითო მცდელობის შემდეგ მცირდება დარჩენილი ცდების რაოდენობა
    
    if user_input == number:          # თუ რიცხვი სწორია
        print("You win!")             # თამაშის მოგება
    elif attempt == 0:                # თუ ცდები დასრულდა და მაინც ვერ გამოიცნო
        print("You lose!")            # წაგება
    else:                             # სხვა შემთხვევაში (არასწორი პასუხი და დარჩენილი ცდები კიდევ არის)
        print("Wrong number!")        # შეტყობინება რომ არასწორია
        print("You still have " + str(attempt) + " tries")  # დარჩენილი ცდების რაოდენობა